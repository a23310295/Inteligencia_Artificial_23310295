#1.1.3 ¿Que compone a un lenguaje?

# Alfabeto: conjunto de simbolos para formar palabras de un lenguaje
# Lexico: Tambien conocido como diccionario, es un conjunto de palabras que el lenguaje ofrece a los usuarios, 
# Ejemplos: una palabra puede no existir en un lenguaje y otra palabra puede existir en varios lenguajes pero tienen diferentes significados
# Sintaxis: Conjunto de reglas que definen la estructura de las oraciones en un lenguaje, es decir, como se combinan las palabras para formar oraciones validas
# Semantica: Determina si una frase tiene sentido, es el significado de las oraciones, es decir, lo que se quiere expresar con ellas

#1.1.4 Lenguaje maquina vs lenguaje de alto nivel

# El IL(listado de instrucciones) es el alfabeto de un lenguaje maquina
# usado para dar comandos a la computadora
# Lenguaje de programacion de alto nivel: es un lenguaje en el que se puede comunicar el lenguaje maquina y el lenguaje natural
# Un programa escrito en un lenguaje de alto nivel se le denomina codigo fuente y al archivo que contiene el codigo se le denomina archivo fuente

# 1.1.5 Compilacion vs Interpretacion

# El programa debe cumplir alfabeticamente, lexicamente, sintacticamente y semanticamente
# Al comenter un error en cualquier punto puedes hacer inutil el programa
# Compilacion: El programa fuente se traduce una vez, esta accion debe repetirse cada vez que se modifique el codigo fuente
# Interpretacion: interpreta el codigo cada vez que se pretende ejecutar 

# 1.1.6 ¿Que hace el interprete?

# un programa finalizado es un archivo de computadora, se invoca al interprete y lee el archivo fuente, el interprete verifica si todas las lineas posteriores son correctas si el compilador encuentra un error, finaliza el trabajo y el resultado es un mensaje de error 
# El interprete te informara donde se encuentra el error y que fue lo que lo causo 

#1.1.7 Compilacion vs Interpretacion – Ventajas y Desventajas

# VENTAJAS Compilacion
# Ejecucion del codigo traducido rapida
# Solo el usuario debe tener el compilador
# El codigo traducido se almacena usando lenguaje maquina, es decir tu codigo seguira siendo tu secreto
# DESVENTAJAS Compilacion
# Debes tener compiladores como plataformas de hardware
# Proceso de compilacion lento 

# VENTAJAS Interpretacion
# Puedes ejecutar el codigo tan pronto como lo completes
# El codigo se almacena usando un lenguaje de programacion
# DESVENTAJAS Interpretacion
# El codigo se ejecuta mas lento que el codigo traducido
# Tanto tu como el usuario final deben tener el interprete para ejecutar el codigo

